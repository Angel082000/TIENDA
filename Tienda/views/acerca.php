Estas creaciones llevan un riguroso proceso. La selección de la hoja y el laborioso sistema de elaboración requiere de agilidad y gran destreza manual para culminar con el teñido de la fibra.

Lo más particular del proceso creador de estas artesanías es que son realizadas en cuevas naturales o excavadas. En tales espacios la temperatura y la humedad favorecen la manipulación de la fibra.
