<h1 style="color: #00145A; font-family:verdana;">Acerca de las artesanías de Campeche</h1>
<h5 style="color: #00145A; font-family:'Carter One', cursive;">Las artesanías típicas de Campeche se caracterizan por la variedad, belleza y calidad de cada una de sus piezas, así como también por sus importantes volúmenes de producción.</h5>
<h6 style="color: #00145A; font-family:'Carter One', cursive;">Esta actividad es el soporte económico de numerosas familias que habitan los municipios de Hopelchén, Calkiní, Campeche, Carmen, Tenabo, Champotón y Hecelchakán.  

    La masiva producción artesanal de Campeche ha conllevado a la modernización de sus sistemas de producción. Este proceso ha destacado por conservar tanto la esteticidad como el concepto de la herencia ancestral en cada una de sus piezas.
    <br>
    En las obras artísticas artesanales campechanas son utilizados el barro, la madera, las fibras naturales y los productos vegetales como materiales primarios.
</h6>
