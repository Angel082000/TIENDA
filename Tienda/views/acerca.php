                <p style="color: #00145A; font-family:verdana;">
                   Las hamacas campechanas son verdaderas obras artísticas que caracterizan a la región de San Antonio, Santa Cruz Pueblo, Concepción, Pucnachén, Sahcabchén y Kobén.

Son tejidas empleando un bastidor de madera, aguja especial e hilos de nailon, algodón y de henequén. Esta misma técnica es utilizada por los artesanos locales en la fabricación de sillas playeras.

Las mujeres indígenas de las comunidades de Santa Rita, Xcupil, Tenabo, Hopelchén, Hecelchakán y Dzibalché elaboran fastuosos bordados (a mano y a máquina) de tiras floreadas en vivos colores.

Tales piezas son utilizadas para adornar huipiles, vestidos, blusas y artículos de mantelería.

Dentro de la amplia gama de obras bordadas destaca la técnica del Renacimiento, que consiste en coser delicadamente los bordes de las figuras; y la técnica del bordado cerrado en la cual el diseño va cubierto totalmente.
